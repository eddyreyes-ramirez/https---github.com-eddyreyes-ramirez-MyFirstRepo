public class Repetition { // class name is Repetition
    public static void main(String[] args) { 
        // count of type integer assigned zero
        int count = 0;
        // while condition is met, count less than 5, print 5 times.
        while (count < 5) { 
            System.out.println("Welcome to Java.");
            count++; //add one to the count and continue loop
        }
    }
}
